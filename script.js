const button = document.getElementById('language-toggle');
let currentLanguage = 'en';

function applyLanguage(language) {
  const dictionary = translations[language];

  document.documentElement.lang = language === 'pt' ? 'pt-br' : 'en';
  document.title = dictionary.pageTitle;

  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) {
    metaDescription.setAttribute('content', dictionary.pageDescription);
  }

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.getAttribute('data-i18n');
    if (dictionary[key]) {
      if (element.hasAttribute('data-i18n-html')) {
        element.innerHTML = dictionary[key];
      } else {
        element.textContent = dictionary[key];
      }
    }
  });

  document.querySelectorAll('[data-i18n-attr]').forEach((element) => {
    const attribute = element.getAttribute('data-i18n-attr');
    const key = element.getAttribute('data-i18n-key');

    if (attribute && key && dictionary[key]) {
      element.setAttribute(attribute, dictionary[key]);
    }
  });

  button.textContent = language === 'en' ? 'PT' : 'EN';
  button.setAttribute('aria-label', language === 'en' ? 'Translate to Portuguese' : 'Traduzir para inglês');
}

button.addEventListener('click', () => {
  currentLanguage = currentLanguage === 'en' ? 'pt' : 'en';
  applyLanguage(currentLanguage);
});

applyLanguage(currentLanguage);